                <li>Обеспечение коммуникации между сотрудниками колледжа по вопросам технической поддержки</li>
                <li>Обработка и учет заявок на обслуживание и ремонт оборудования</li>
                <li>Ведение истории обращений и взаимодействий с Пользователями</li>
                <li>Улучшение качества обслуживания и работы Системы</li>
                <li>Отправка уведомлений, связанных с работой Системы</li>
