            <p>5.1. Обработка персональных данных осуществляется на законной и справедливой основе.</p>

            <p>5.2. Обработка персональных данных ограничивается достижением конкретных, заранее определенных и законных целей.</p>

            <p>5.3. Не допускается обработка персональных данных, несовместимая с целями сбора персональных данных.</p>

            <p>5.4. Не допускается объединение баз данных, содержащих персональные данные, обработка которых осуществляется в целях, несовместимых между собой.</p>

            <p>5.5. Обработке подлежат только персональные данные, которые отвечают целям их обработки.</p>

            <p>5.6. Содержание и объем обрабатываемых персональных данных соответствуют заявленным целям обработки.</p>

